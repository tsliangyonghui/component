<template>
  <div class="el-checkbox-group" role="group">
    <slot></slot>
  </div>
</template>

<script>
import Emitter from 'element-ui/src/mixins/emitter'
export default {
  name: 'MCheckboxGroup',
  componentName: 'MCheckboxGroup',
  mixins: [Emitter],
  inject: {
    elFormItem: {
      default: ''
    }
  },
  props: {
    value: {},
    disabled: Boolean,
    min: Number,
    max: Number,
    size: String,
    fill: String,
    textColor: String
  },

  computed: {
    _elFormItemSize() {
      return (this.elFormItem || {}).elFormItemSize
    },
    checkboxGroupSize() {
      return this.size || this._elFormItemSize
    }
  },

  watch: {
    value(value) {
      this.dispatch('ElFormItem', 'el.form.change', [value])
    }
  }
}
</script>

